def usage():
    print()
    print("===============================================================================")
    print("optool - dust opacities from the command line")
    print("         Dominik, Min, Tazaki 2021, https://ascl.net/2104.010, version 1.9.14")
    print()
    print("-c                        List available materials")
    print("-c KEY-or-FILE [Mfrac]    Add material with mass fraction. -c may be omitted")
    print("-m KEY-or-FILE [Mfrac]    Add material with mass fraction in mantle")
    print("-p POROSITY [PMANTLE]     Set porosity, possibly different for core and mantle")
    print("-dhs [FMAX]               Maximum volume fraction of vacuum in DHS computation")
    print("-mmf [A0 [DF-or-FILL]]    Use MMF with monomer size A0 and fractal dim or fill")
    print("-a AMIN [AMAX [SD [NA]]]  Grain size [micron] dist. SD=P (>PL) or M:S (>log-n)")
    print("-l LMIN [LMAX [NLAM]]     Set up wavelength grid  (units for a and l: microns)")
    print("-a FILE; -l FILE          Read size distribution or lambda grid from a file")
    print("-d [NSUB]                 Write NA files for specific grain sizes")
    print("-s [NANG]                 Add scattering matrix for NANG angles to output")
    print("-chop [NDEG]              Remove NDEG degrees from the forward-scattering peak")
    print("-o [DIRECTORY]            Output to DIRECTORY instead of current working dir")
    print("-radmc [LABEL]            Output as RADMC-3D input file")
    print("-fits; -print [Var]       Output to FITS file, or to STDOUT")
    print("-h [OPT]; -man            Show this msg or help on -OPT; Show the full manual")
    print("-q; -v                    Quiet or more verbose on STDOUT")
    print("-xlim XLIM                Switch to Mie for speed at x=XLIM")
    print("===============================================================================")


